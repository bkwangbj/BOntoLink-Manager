/**
 * 把 'antd:xxx' 形式的 key 翻译成中文,未匹配返回 null。
 */
export function antdZhName(name) {
  if (!name || typeof name !== 'string') return null
  const m = name.match(/^antd:([a-z0-9-]+)/)
  if (!m) return null
  return ANTD_ZH[m[1]] || null
}
